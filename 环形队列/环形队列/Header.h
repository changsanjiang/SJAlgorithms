#ifndef Header_h
#define Header_h

/**
 队列:
    是一种线性表, 它只允许在表的前端(front)进行删除操作, 而在表的后端(rear)进行插入操作.
    进行删除操作的端称为对头.
    进行插入操作的端称为对尾巴.
 
 环形队列:
    当数据指针到达线性表的尾部时, 就将他转到0位置重新开始.
 
 场景:
    存储日志记录, 只存储1000条日志记录.
 */

#endif /* Header_h */
